      h2 1.1	Definición de logística
    
    p.m-0.mb-4 Para que un producto llegue a su destino, tuvo que pasar por un extenso proceso que va desde la obtención de las materias primas, el embalaje, transporte, recepción, inspección, clasificación, transformación, almacenamiento y entrega al cliente, entre otros asuntos. Esta red de medios, métodos e infraestructura garantiza la conservación y calidad de un producto o servicio. Esto es lo que se conoce como logística.
